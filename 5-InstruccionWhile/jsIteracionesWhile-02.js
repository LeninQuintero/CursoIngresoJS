/*
al presionar el botón mostrar 10 repeticiones 
con números DESCENDENTES, desde el 10 al 1.*/
function mostrar()
{
	let numeroDescendente;
	numeroDescendente = 10;

	while (numeroDescendente > 0) {
		console.log('El numero descendente es: ' + numeroDescendente);
		numeroDescendente--;
	}

}//FIN DE LA FUNCIÓN